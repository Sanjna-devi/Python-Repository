numbers = [1,2,3,4,5]

# Slicing the list from index 1 to index 3
# The last index (4) is not included
print(numbers[1:4])

# Printing elements from the start up to index 2
# If the starting index is not given, Python starts from 0
print(numbers[:3])

# Printing elements from index 2 to the end of the list
# If the ending index is not given, Python goes till the last element
print(numbers[2:])

# -3 means third element from the end, -2 means second from the end
# The last index (-2) is not included
print(numbers[-3:-2])