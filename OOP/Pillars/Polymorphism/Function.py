#Function Polymorphism in Python
# Function polymorphism means the same function can work
# with different data types or objects.

# len() works with a string
print(len("Hello"))

# len() works with a list
print(len([1, 2, 3, 4]))

# Explanation:
# The same function 'len()' behaves differently depending on the data type.
# This is an example of polymorphism in Python.