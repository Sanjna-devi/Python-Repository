#Operator Polymorphism in Python
# Operator polymorphism (or operator overloading) occurs when
# the same operator behaves differently with different data types.

# + operator with numbers → addition
print(5 + 3)        

# + operator with strings → concatenation
print("Hi " + "Sanjna")   

# Explanation:
# The + operator behaves differently depending on the data type.
# This is an example of polymorphism in Python.

