# Demonstrates assignment operators in Python

a = 50      # initialize variable a
a += 10     # add 10 to a and store result in a

b = 30      # initialize variable b
b -= 10     # subtract 10 from b and store result in b

c = 10      # initialize variable c
c *= 20     # multiply c by 20 and store result in c

print(a)    # prints updated value of a
print(b)    # prints updated value of b
print(c)    # prints updated value of c